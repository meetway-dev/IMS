export const designTokens = {
  spacing,
  borderRadius,
  shadows,
  colors,
  typography,
  transitions,
  zIndex,
  breakpoints,
  
  // Utility functions
  pxToRem: (px: number): string => `${px / 16}rem`,
  remToPx: (rem: number): string => `${rem * 16}px`,
  
  // Responsive helpers
  mediaQuery: (breakpoint: keyof typeof breakpoints, rule: string): string => 
    `@media (min-width: ${breakpoints[breakpoint]}) { ${rule} }`,
  
  // Color helpers
  rgba: (color: string, opacity: number): string => {
    if (color.startsWith('hsl')) {
      return color.replace(')', `, ${opacity})`).replace('hsl', 'hsla');
    }
    return color;
  },
} as const;